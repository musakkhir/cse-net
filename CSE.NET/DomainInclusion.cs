namespace Google.CustomSearch
{
    /// <summary>
    /// Specifies wheather a domain specified in AdvancedSearchSite should
    /// be included or excluded from the results.
    /// </summary>
    public enum DomainInclusion
    {
        /// <summary>
        /// The site should be included in the results.
        /// </summary>
        Include,

        /// <summary>
        /// The site should be excluded from the results.
        /// </summary>
        Exclude
    }
}
